#include "libft.h"

void	*ft_calloc(size_t nitems, size_t size)
{
	void	*mem;

	mem = malloc(nitems * size);
	if (mem != NULL)
	{
		ft_memset(mem, 0, (nitems * size));
	}
	return (mem);
}

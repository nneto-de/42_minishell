#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	c1;

	i = ft_strlen(s);
	c1 = c;
	while (i >= 0)
	{
		if (s[i] == c1)
		{
			return ((char *)(s + i));
		}
		i--;
	}
	return (NULL);
}

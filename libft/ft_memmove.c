#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	int		i;
	char	*s;
	char	*d;

	i = 0;
	s = (char *)src;
	d = (char *)dest;
	if ((dest == NULL) && (src == NULL))
		return (NULL);
	if (d > s)
	{
		while (n--)
		{
			*(d + n) = *(s + n);
		}
	}
	else
	{
		while (n--)
		{
			*(d + i) = *(s + i);
			i++;
		}
	}
	return (dest);
}
